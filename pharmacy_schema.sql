-- ============================================================
-- Pharmacy Reservation System Database Setup
-- Run this in the Supabase SQL Editor
-- ============================================================

-- 1. Products (Medicines & Supplies) Table
CREATE TABLE IF NOT EXISTS products (
    id SERIAL PRIMARY KEY,
    name TEXT NOT NULL,
    price NUMERIC NOT NULL,
    stock INT DEFAULT 0,
    category TEXT NOT NULL,
    image_url TEXT,
    available BOOLEAN DEFAULT true,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 2. Reservations Table (for customer orders/bookings)
CREATE TABLE IF NOT EXISTS reservations (
    id SERIAL PRIMARY KEY,
    reservation_number TEXT UNIQUE NOT NULL, -- Example: RES-123456
    customer_name TEXT NOT NULL,
    customer_phone TEXT,
    status TEXT DEFAULT 'pending' CHECK (status IN ('pending', 'confirmed', 'completed', 'cancelled')),
    total_amount NUMERIC DEFAULT 0,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 3. Reservation Items Table (line items for each reservation)
CREATE TABLE IF NOT EXISTS reservation_items (
    id SERIAL PRIMARY KEY,
    reservation_id INT REFERENCES reservations(id) ON DELETE CASCADE,
    product_id INT REFERENCES products(id),
    quantity INT DEFAULT 1,
    unit_price NUMERIC NOT NULL,
    total_price NUMERIC NOT NULL,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 4. Customer Feedback / Contact Messages
CREATE TABLE IF NOT EXISTS customer_feedback (
    id SERIAL PRIMARY KEY,
    customer_name TEXT,
    customer_phone TEXT,
    rating INT CHECK (rating >= 1 AND rating <= 5),
    message TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 5. Pending Reservations Table (for admin approval flow)
CREATE TABLE IF NOT EXISTS pending_reservations (
    id SERIAL PRIMARY KEY,
    reservation_data JSONB,
    items_data JSONB,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- ============================================================
-- RLS (Row Level Security) Setup (Optional but recommended)
-- ============================================================
-- ENABLE RLS
ALTER TABLE products ENABLE ROW LEVEL SECURITY;
ALTER TABLE reservations ENABLE ROW LEVEL SECURITY;
ALTER TABLE reservation_items ENABLE ROW LEVEL SECURITY;
ALTER TABLE pending_reservations ENABLE ROW LEVEL SECURITY;

-- Allow public read access to products
CREATE POLICY "Public profiles are viewable by everyone." 
ON products FOR SELECT 
USING (true);

-- Allow public inserts for reservations (so anyone can book)
CREATE POLICY "Anyone can insert a reservation" 
ON reservations FOR INSERT 
WITH CHECK (true);

CREATE POLICY "Anyone can insert reservation items" 
ON reservation_items FOR INSERT 
WITH CHECK (true);

-- Allow public inserts for pending reservations
CREATE POLICY "Anyone can insert pending reservations" 
ON pending_reservations FOR INSERT 
WITH CHECK (true);

-- Note: Depending on your real app, you might want only authenticated Admins to SELECT/DELETE from pending
CREATE POLICY "Public can do all in pending_reservations for demo" 
ON pending_reservations FOR ALL 
USING (true);

